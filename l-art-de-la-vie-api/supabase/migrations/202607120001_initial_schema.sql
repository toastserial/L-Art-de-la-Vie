create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  insert into public.profiles (id, full_name)
  values (new.id, coalesce(new.raw_user_meta_data ->> 'full_name', new.email));
  return new;
end;
$$;

create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.is_store_member(requested_store_id uuid)
returns boolean
language sql
stable
security definer set search_path = ''
as $$
  select exists (
    select 1 from public.store_members
    where store_id = requested_store_id and user_id = (select auth.uid())
  );
$$;

create or replace function public.can_manage_store(requested_store_id uuid)
returns boolean
language sql
stable
security definer set search_path = ''
as $$
  select exists (
    select 1 from public.store_members
    where store_id = requested_store_id
      and user_id = (select auth.uid())
      and role in ('owner', 'admin')
  );
$$;

-- Registra entradas/salidas y cambia stock dentro de la misma transacción.
create or replace function public.register_inventory_movement(
  requested_store_id uuid,
  requested_product_id uuid,
  movement_type public.inventory_movement_type,
  movement_quantity integer,
  movement_note text default null
)
returns public.inventory_movements
language plpgsql
security invoker
set search_path = ''
as $$
declare
  selected_product public.products;
  created_movement public.inventory_movements;
begin
  if movement_type not in ('entrada', 'salida') then
    raise exception 'Solo se permiten movimientos de entrada o salida';
  end if;
  if movement_quantity <= 0 then raise exception 'La cantidad debe ser positiva'; end if;

  select * into selected_product from public.products
  where id = requested_product_id and store_id = requested_store_id and active
  for update;
  if not found then raise exception 'Producto no encontrado'; end if;
  if movement_type = 'salida' and selected_product.stock < movement_quantity then
    raise exception 'Stock insuficiente para %', selected_product.name;
  end if;

  update public.products set stock = stock + case when movement_type = 'entrada' then movement_quantity else -movement_quantity end
  where id = requested_product_id;
  insert into public.inventory_movements (store_id, product_id, created_by, type, quantity, product_name, note)
  values (requested_store_id, requested_product_id, (select auth.uid()), movement_type, movement_quantity, selected_product.name, nullif(trim(movement_note), ''))
  returning * into created_movement;
  return created_movement;
end;
$$;

-- El servidor envía [{"productId":"uuid","quantity":2}]. Precios y stock se resuelven aquí.
create or replace function public.complete_sale(
  requested_store_id uuid,
  requested_items jsonb,
  requested_payment_method public.payment_method,
  discount_percent numeric default 0,
  requested_cash_received numeric default null
)
returns jsonb
language plpgsql
security invoker
set search_path = ''
as $$
declare
  requested_item jsonb;
  selected_product public.products;
  item_quantity integer;
  calculated_subtotal numeric(12,2) := 0;
  calculated_discount numeric(12,2);
  calculated_total numeric(12,2);
  calculated_change numeric(12,2);
  created_sale public.sales;
begin
  if jsonb_typeof(requested_items) <> 'array' or jsonb_array_length(requested_items) = 0 then
    raise exception 'La venta no contiene productos';
  end if;
  if discount_percent < 0 or discount_percent > 100 then raise exception 'Descuento inválido'; end if;

  for requested_item in select * from jsonb_array_elements(requested_items)
  loop
    item_quantity := (requested_item ->> 'quantity')::integer;
    if item_quantity <= 0 then raise exception 'Cantidad inválida'; end if;
    select * into selected_product from public.products
    where id = (requested_item ->> 'productId')::uuid and store_id = requested_store_id and active
    for update;
    if not found then raise exception 'Producto no encontrado'; end if;
    if selected_product.stock < item_quantity then raise exception 'Stock insuficiente para %', selected_product.name; end if;
    calculated_subtotal := calculated_subtotal + selected_product.price * item_quantity;
  end loop;

  calculated_discount := round(calculated_subtotal * discount_percent / 100, 2);
  calculated_total := calculated_subtotal - calculated_discount;
  if requested_payment_method = 'efectivo' then
    if requested_cash_received is null or requested_cash_received < calculated_total then raise exception 'Efectivo insuficiente'; end if;
    calculated_change := requested_cash_received - calculated_total;
  elsif requested_cash_received is not null then
    raise exception 'cash_received solo aplica a efectivo';
  end if;

  insert into public.sales (store_id, sold_by, subtotal, discount, total, payment_method, cash_received, change_amount)
  values (requested_store_id, (select auth.uid()), calculated_subtotal, calculated_discount, calculated_total,
          requested_payment_method, requested_cash_received, calculated_change)
  returning * into created_sale;

  for requested_item in select * from jsonb_array_elements(requested_items)
  loop
    item_quantity := (requested_item ->> 'quantity')::integer;
    select * into selected_product from public.products where id = (requested_item ->> 'productId')::uuid for update;
    insert into public.sale_items (sale_id, product_id, product_name, quantity, unit_price)
    values (created_sale.id, selected_product.id, selected_product.name, item_quantity, selected_product.price);
    update public.products set stock = stock - item_quantity where id = selected_product.id;
    insert into public.inventory_movements (store_id, product_id, sale_id, created_by, type, quantity, product_name)
    values (requested_store_id, selected_product.id, created_sale.id, (select auth.uid()), 'venta', item_quantity, selected_product.name);
  end loop;

  return jsonb_build_object('id', created_sale.id, 'folio', created_sale.folio, 'subtotal', created_sale.subtotal,
    'discount', created_sale.discount, 'total', created_sale.total, 'paymentMethod', created_sale.payment_method,
    'cashReceived', created_sale.cash_received, 'change', created_sale.change_amount, 'date', created_sale.created_at);
end;
$$;

create or replace function public.close_cash(requested_store_id uuid, requested_actual_cash numeric)
returns public.cash_closes
language plpgsql
security invoker
set search_path = ''
as $$
declare
  local_date date;
  cash_total numeric(12,2);
  card_total numeric(12,2);
  transfer_total numeric(12,2);
  expenses_total numeric(12,2);
  created_close public.cash_closes;
begin
  if requested_actual_cash < 0 then raise exception 'El efectivo real no puede ser negativo'; end if;
  select (now() at time zone timezone)::date into local_date from public.stores where id = requested_store_id;
  if local_date is null then raise exception 'Tienda no encontrada'; end if;

  select
    coalesce(sum(total) filter (where payment_method = 'efectivo'), 0),
    coalesce(sum(total) filter (where payment_method = 'tarjeta'), 0),
    coalesce(sum(total) filter (where payment_method = 'transferencia'), 0)
  into cash_total, card_total, transfer_total
  from public.sales
  where store_id = requested_store_id and (created_at at time zone (select timezone from public.stores where id = requested_store_id))::date = local_date;

  select coalesce(sum(e.amount), 0) into expenses_total
  from public.expenses e
  left join public.cash_close_expenses cce on cce.expense_id = e.id
  where e.store_id = requested_store_id and cce.expense_id is null
    and (e.created_at at time zone (select timezone from public.stores where id = requested_store_id))::date = local_date;

  insert into public.cash_closes (store_id, business_date, closed_by, total_sales, cash_sales, card_sales,
    transfer_sales, total_expenses, expected_cash, actual_cash)
  values (requested_store_id, local_date, (select auth.uid()), cash_total + card_total + transfer_total,
    cash_total, card_total, transfer_total, expenses_total, cash_total - expenses_total, requested_actual_cash)
  returning * into created_close;

  insert into public.cash_close_expenses (cash_close_id, expense_id)
  select created_close.id, e.id from public.expenses e
  left join public.cash_close_expenses cce on cce.expense_id = e.id
  where e.store_id = requested_store_id and cce.expense_id is null
    and (e.created_at at time zone (select timezone from public.stores where id = requested_store_id))::date = local_date;
  return created_close;
end;
$$;

