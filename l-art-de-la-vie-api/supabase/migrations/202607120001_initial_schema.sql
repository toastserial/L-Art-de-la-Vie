-- L'Art de la Vie POS - Supabase/PostgreSQL schema
-- Ejecutar en Supabase SQL Editor o con: supabase db push

create extension if not exists pgcrypto;

create type public.product_category as enum ('Decoración', 'Perfumes', 'Carteras', 'Varios');
create type public.payment_method as enum ('efectivo', 'tarjeta', 'transferencia');
create type public.inventory_movement_type as enum ('entrada', 'salida', 'venta');
create type public.store_role as enum ('owner', 'admin', 'cashier');

create table public.stores (
  id uuid primary key default gen_random_uuid(),
  name text not null check (length(trim(name)) between 1 and 120),
  currency char(3) not null default 'HNL',
  timezone text not null default 'America/Tegucigalpa',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.store_members (
  store_id uuid not null references public.stores(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  role public.store_role not null default 'cashier',
  created_at timestamptz not null default now(),
  primary key (store_id, user_id)
);

create table public.products (
  id uuid primary key default gen_random_uuid(),
  store_id uuid not null references public.stores(id) on delete cascade,
  code text not null check (length(trim(code)) between 1 and 50),
  name text not null check (length(trim(name)) between 1 and 160),
  category public.product_category not null,
  price numeric(12,2) not null check (price >= 0),
  stock integer not null default 0 check (stock >= 0),
  min_stock integer not null default 0 check (min_stock >= 0),
  image_url text,
  active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (store_id, code)
);

create table public.sales (
  id uuid primary key default gen_random_uuid(),
  store_id uuid not null references public.stores(id) on delete restrict,
  folio bigint generated by default as identity,
  sold_by uuid references auth.users(id) on delete set null,
  subtotal numeric(12,2) not null check (subtotal >= 0),
  discount numeric(12,2) not null default 0 check (discount >= 0 and discount <= subtotal),
  total numeric(12,2) not null check (total >= 0 and total = subtotal - discount),
  payment_method public.payment_method not null,
  cash_received numeric(12,2),
  change_amount numeric(12,2),
  created_at timestamptz not null default now(),
  constraint valid_cash_payment check (
    (payment_method = 'efectivo' and cash_received is not null and cash_received >= total and change_amount = cash_received - total)
    or (payment_method <> 'efectivo' and cash_received is null and change_amount is null)
  ),
  unique (store_id, folio)
);

create table public.sale_items (
  id uuid primary key default gen_random_uuid(),
  sale_id uuid not null references public.sales(id) on delete cascade,
  product_id uuid not null references public.products(id) on delete restrict,
  product_name text not null,
  quantity integer not null check (quantity > 0),
  unit_price numeric(12,2) not null check (unit_price >= 0),
  subtotal numeric(12,2) generated always as (quantity * unit_price) stored,
  unique (sale_id, product_id)
);

create table public.inventory_movements (
  id uuid primary key default gen_random_uuid(),
  store_id uuid not null references public.stores(id) on delete cascade,
  product_id uuid not null references public.products(id) on delete restrict,
  sale_id uuid references public.sales(id) on delete restrict,
  created_by uuid references auth.users(id) on delete set null,
  type public.inventory_movement_type not null,
  quantity integer not null check (quantity > 0),
  product_name text not null,
  note text,
  created_at timestamptz not null default now(),
  constraint sale_movement_reference check (
    (type = 'venta' and sale_id is not null) or (type <> 'venta' and sale_id is null)
  )
);

create table public.expenses (
  id uuid primary key default gen_random_uuid(),
  store_id uuid not null references public.stores(id) on delete cascade,
  created_by uuid references auth.users(id) on delete set null,
  description text not null check (length(trim(description)) between 1 and 300),
  amount numeric(12,2) not null check (amount > 0),
  created_at timestamptz not null default now()
);

create table public.cash_closes (
  id uuid primary key default gen_random_uuid(),
  store_id uuid not null references public.stores(id) on delete restrict,
  business_date date not null,
  closed_by uuid references auth.users(id) on delete set null,
  total_sales numeric(12,2) not null check (total_sales >= 0),
  cash_sales numeric(12,2) not null check (cash_sales >= 0),
  card_sales numeric(12,2) not null check (card_sales >= 0),
  transfer_sales numeric(12,2) not null check (transfer_sales >= 0),
  total_expenses numeric(12,2) not null check (total_expenses >= 0),
  expected_cash numeric(12,2) not null,
  actual_cash numeric(12,2) not null check (actual_cash >= 0),
  difference numeric(12,2) generated always as (actual_cash - expected_cash) stored,
  created_at timestamptz not null default now(),
  unique (store_id, business_date)
);

create table public.cash_close_expenses (
  cash_close_id uuid not null references public.cash_closes(id) on delete cascade,
  expense_id uuid not null references public.expenses(id) on delete restrict,
  primary key (cash_close_id, expense_id),
  unique (expense_id)
);

create index products_store_active_idx on public.products (store_id, active);
create index products_store_category_idx on public.products (store_id, category);
create index sales_store_created_idx on public.sales (store_id, created_at desc);
create index sale_items_sale_idx on public.sale_items (sale_id);
create index sale_items_product_idx on public.sale_items (product_id);
create index movements_store_created_idx on public.inventory_movements (store_id, created_at desc);
create index movements_product_idx on public.inventory_movements (product_id, created_at desc);
create index expenses_store_created_idx on public.expenses (store_id, created_at desc);
create index cash_closes_store_date_idx on public.cash_closes (store_id, business_date desc);
create index store_members_user_idx on public.store_members (user_id, store_id);

